      , implication . concat $
        [ [constructorType <+> envVar <+> arguments i <+> nextAddrVar <+> stateVar <+> nextAddrVar']
        , [invPropVar <+> envVar <+> arguments i <+> nextAddrVar <+> stateVar]
        ]
      ]

-- | Definition of invariant claim for behaviour cases
