      , implication . concat $
        [ [initPrecsType <+> envVar <+> arguments i <+> nextAddrVar ]
        , [stepType <+> stateVar <+> stateVar']
        , [invPropVar <+> envVar <+> arguments i <+> nextAddrVar <+> stateVar]
        , [invPropVar <+> envVar <+> arguments i <+> nextAddrVar <+> stateVar']
        ]
